    private void jBtnConsultarMslActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarMslActionPerformed
        if (jTxtNomeMsl.getText().isEmpty() && jTxtPlataformalMsl.getText().isEmpty()) {
            List lista = jogosDAO.listNome(jTxtNomeMsl.getText());
            jogosControle.setList(lista);
        } else {
            if (!jTxtNomeMsl.getText().isEmpty() && !jTxtPlataformalMsl.getText().isEmpty()) {
                double plataformaMsl = Double.parseDouble(jTxtPlataformalMsl.getText());
                List lista = jogosDAO.listNomePlataforma(jTxtNomeMsl.getText(),plataformaMsl);
                jogosControle.setList(lista);
            } else {
                if (!jTxtNomeMsl.getText().isEmpty()) {
                    List lista = jogosDAO.listNome(jTxtNomeMsl.getText());
                    jogosControle.setList(lista);
                }
                if (!jTxtPlataformalMsl.getText().isEmpty()) {
                    double plataformaMsl = Double.parseDouble(jTxtPlataformalMsl.getText());
                    List lista = jogosDAO.listPlataforma(plataformaMsl);
                    jogosControle.setList(lista);
                }
            }
        }
    }//GEN-LAST:event_jBtnConsultarMslActionPerformed

