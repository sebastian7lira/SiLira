    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
    ClienteMsl clienteMsl = viewBean();
    ClienteDAO clientesDAO = new ClienteDAO();
    
    if (incl == true) {
        clientesDAO.insert(clienteMsl);
        UtilMsl.mensagem("Incluido com sucesso.");
    } else {
        clientesDAO.update(clienteMsl);
        UtilMsl.mensagem("Alterado com sucesso.");
        }
    
        UtilMsl.habilitar(false,jTxtCodigo, jTxtNome, jTxtSobrenome, jTxtSexo,jFmtRg,jFmtCpf,jFmtNascimento,jTxtEmail,jTxtTelefone,jTxtCelular,jTxtCep,jTxtEndereco,jTxtBairro,jTxtCidade,jTxtCivil);
        UtilMsl.habilitar(true, jBtnIncluir, jBtnPesquisar);
        UtilMsl.limparCampos(jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,jTxtCodigo, jTxtNome, jTxtSobrenome, jTxtSexo,jFmtRg,jFmtCpf,jFmtNascimento,jTxtEmail,jTxtTelefone,jTxtCelular,jTxtCep,jTxtEndereco,jTxtBairro,jTxtCidade,jTxtCivil);

    }//GEN-LAST:event_jBtnConfirmarActionPerformed

