    private void jBtnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOkActionPerformed
        jogosMsl = viewBean();
        jogosDAO = new JogosDAO();
        
        if (incl = true) {
            jogosDAO.insert(jogosMsl);
            UtilMsl.mensagem("Adicionado com sucesso!");
        } else {
            jogosDAO.update(jogosMsl);
            UtilMsl.mensagem("Atualizado com sucesso!");
        }
        setVisible(false);
    }//GEN-LAST:event_jBtnOkActionPerformed

