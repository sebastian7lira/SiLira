        jFmtDataNacimento.setText(dataNascimento);//set data
        
        JTxtNome.setText(usuariosMsl.getNomeMsl());//set txt
        JTxtApelido.setText(usuariosMsl.getApelidoMsl());//set txt
        jFmtCpf.setText(usuariosMsl.getCpfMsl());//set txt
        jPwfSenha.setText(usuariosMsl.getSenhaMsl());//set txt
        
        
      //  String nivel = usuariosMsl.getNivelMsl();//set combo
        
        //if(nivel.equals("1")){
          //  JCboNivel.setSelectedIndex(0);
        //}else {
          //  JCboNivel.setSelectedIndex(1);
