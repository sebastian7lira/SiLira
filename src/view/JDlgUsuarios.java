    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
    if (UtilMsl.perguntar("Excluir o registro?") == true) {
        UsuariosMsl usuariosMsl = viewBean();
        UsuariosDAO usuariosDAO = new UsuariosDAO();
        usuariosDAO.delete(usuariosMsl);
        UtilMsl.mensagem("Excluido.");
    }else{
        UtilMsl.mensagem("Cancelado.");
       }
        UtilMsl.limparCampos(JTxtCodigo,jFmtCpf,jChbAtivo,JTxtNome,jFmtDataNacimento,JTxtApelido,JCboNivel,jPwfSenha);
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
    UsuariosMsl usuarioMsl = viewBean();
    UsuariosDAO usuarioDAO = new UsuariosDAO();
    
    if (incl == true) {
        usuarioDAO.insert(usuarioMsl);
        UtilMsl.mensagem("Incluido com sucesso.");
    } else {
        usuarioDAO.update(usuarioMsl);
        UtilMsl.mensagem("Alterado com sucesso.");
        }
    
        UtilMsl.habilitar(false, jBtnAlterar,jBtnCancelar,jBtnConfirmar,jBtnExcluir,JTxtCodigo,jFmtCpf,jChbAtivo,JTxtNome,jFmtDataNacimento,JTxtApelido,JCboNivel,jPwfSenha);
        UtilMsl.habilitar(true, jBtnIncluir,jBtnPesquisar);
        UtilMsl.limparCampos(JTxtCodigo,jFmtCpf,jChbAtivo,JTxtNome,jFmtDataNacimento,JTxtApelido,JCboNivel,jPwfSenha);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

